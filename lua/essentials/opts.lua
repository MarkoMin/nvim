-- Visible line numbers (current + absolute)
vim.opt.nu = true
vim.opt.rnu = true

-- Tab equals to 4 spaces
vim.opt.tabstop = 4
vim.opt.shiftwidth = 4
vim.opt.expandtab = true

-- Highlight search matches while typing
vim.opt.incsearch = true

-- better splitting
vim.opt.splitbelow = true
vim.opt.splitright = true

-- Following two options perform: case-insensitive search if pattern is all-lowercase, case-sensitive search otherwise
vim.opt.ignorecase = true
vim.opt.smartcase = true

-- enable 24bit colors
vim.opt.termguicolors = true

-- enable undo throughtout previous file saves :D
vim.opt.undofile = true

-- current line Highlight
vim.opt.cursorline = true 

